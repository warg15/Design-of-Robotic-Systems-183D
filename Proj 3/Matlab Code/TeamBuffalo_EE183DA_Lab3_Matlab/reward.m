function reward_disp = reward(state_c)
    % ===== Grid World Reward ===== %
    % REFER to the grid world shown in the lab instruction
    gw_reward = [-100 -100 -100 -100 -100 -100;...
                 -100 0    0    -10  1   -100;...
                 -100 0    0    -10  0    -100;...
                 -100 0    0    0    0    -100;...
                 -100 0    0    0    0    -100;...
                 -100 -100 -100 -100 -100 -100];
    [rows,cols] = size(gw_reward);
    
    % ===== Map the reward value ===== %
    reward_disp = gw_reward((rows+1) - state_c(2),state_c(1));
end